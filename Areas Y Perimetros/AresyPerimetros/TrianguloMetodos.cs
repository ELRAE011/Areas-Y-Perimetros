using System;

namespace AresyPerimetros
{
	public class TrianguloMetodos
	{
		public TrianguloMetodos()
		{
		}
		
		public float CalcularArea(float base1,float altura){
			return (base1*altura)/2;
		}
		public float CalcularPerimetro(float base1){
			return base1*3;
		}
	}
}
